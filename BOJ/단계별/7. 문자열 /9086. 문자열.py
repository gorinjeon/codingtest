'''
입력
입력의 첫 줄에는 테스트 케이스의 개수 T(1 ≤ T ≤ 10)가 주어진다. 각 테스트 케이스는 한 줄에 하나의 문자열이 주어진다. 문자열은 알파벳 A~Z 대문자로 이루어지며 알파벳 사이에 공백은 없으며 문자열의 길이는 1000보다 작다.

출력
각 테스트 케이스에 대해서 주어진 문자열의 첫 글자와 마지막 글자를 연속하여 출력한다.
'''
# 내 코드
for _ in range(int(input())):
    s = input()
    print(s[0]+s[-1])

# 다른 답
for k in [*open(0)][1:]:
    print(k[0] + k[-2])
'''
open(0): 표준 입력(stdin)을 읽기 위해 엽니다. 일반적인 스크립트에서는 콘솔에서 입력을 받거나 스크립트로 리디렉션된 파일에서 입력을 받을 수 있습니다.
[*open(0)]: 표준 입력에서 모든 줄을 읽어 리스트로 만듭니다. [*...] 구문은 줄을 리스트로 언패킹하는 방법입니다.
[1:]: 리스트를 슬라이스하여 첫 번째 줄을 제외합니다. 따라서 두 번째 줄부터 처리합니다.
for k in ...: 첫 번째 줄을 제외한 각 줄을 반복합니다.
k[0]: 현재 줄의 첫 번째 문자를 가져옵니다.
k[-2]: 현재 줄의 끝에서 두 번째 문자를 가져옵니다.
'''
